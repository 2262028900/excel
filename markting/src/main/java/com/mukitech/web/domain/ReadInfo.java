package com.mukitech.web.domain;

public class ReadInfo {
	private String compName;
	private String conaName;
	private String comptype;
	private String compAddr;
	private String compPhon;
	private String compDate;
	
	public String getCompName() {
		return compName;
	}
	public void setCompName(String compName) {
		this.compName = compName;
	}
	public String getConaName() {
		return conaName;
	}
	public void setConaName(String conaName) {
		this.conaName = conaName;
	}
	public String getComptype() {
		return comptype;
	}
	public void setComptype(String comptype) {
		this.comptype = comptype;
	}
	public String getCompAddr() {
		return compAddr;
	}
	public void setCompAddr(String compAddr) {
		this.compAddr = compAddr;
	}
	public String getCompPhon() {
		return compPhon;
	}
	public void setCompPhon(String compPhon) {
		this.compPhon = compPhon;
	}
	public String getCompDate() {
		return compDate;
	}
	public void setCompDate(String compDate) {
		this.compDate = compDate;
	}

}
